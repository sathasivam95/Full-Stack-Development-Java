public class TestLoops1 {

    public static void main(String[] args) {

        //do-while loops --> do something then check condition, it means it will run at least once even
        // if the condition is false.
        int counter = 1;

        do{
            System.out.println("Hello World!!!");
            counter = counter + 1;
        }while (counter <=5);

    }
}
